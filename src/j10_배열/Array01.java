package j10_배열;

public class Array01 {

	public static void main(String[] args) {
//		정수 배열
		int[] num = new int[5];
		
//		문자열 배열
		String[] str = new String[5];
		
		str[0] = "가";
		str[1] = "나";
		str[2] = "다";
		str[3] = "라";
		str[4] = "마";
		
		num[0] = 10;
		num[1] = 11;
		num[2] = 12;
		num[3] = 13;
		num[4] = 14;
		
		System.out.println(num[0]);
		System.out.println(num[1]);
		System.out.println(num[2]);
	}

}
